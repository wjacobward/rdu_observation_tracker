def get_airport_city(iata_code: str | None) -> tuple[str, str] | None:
    if not iata_code:
        return None
    return AIRPORT_CITIES.get(iata_code.upper())


def get_city_name(iata_code: str | None) -> str:
    result = get_airport_city(iata_code)
    if result:
        return result[0]
    return iata_code or "Unknown"
